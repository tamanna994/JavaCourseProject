package Practice;

public class prac11_DataTypes {

    public static void main(String[] args) {


         // Primitive Data_Type - Boolean


        Boolean booleanValue = true;
        System.out.println("Boolean Output: "+booleanValue);


         // Primitive Data_Type - byte

        byte byteValue = 30;
        System.out.println("Byte Output: "+byteValue);


          //Primitive Data_Type - short

        short shortValue = 6000;
        System.out.println("Short Output: "+shortValue);


         // Primitive Data_Type - short

        int intValue = 40000;
        System.out.println("Integer Output: "+intValue);


         // Primitive Data_Type - short

        long longValue = 300000;
        System.out.println("Long Output: "+longValue);


         // Primitive Data_Type - short

        float floatValue = 4.22f;
        System.out.println("Float Output: "+floatValue);


         // Primitive Data_Type - short

        double doubleValue = 22.544;
        System.out.println("Double Output: "+doubleValue);


         // Primitive Data_Type - short

        char character = 'e';
        System.out.println("Char Output: "+character);
    }

}
